    public void luuDanhSachVaoFile() {
        try {
            File file = new File(tenFile);
            if (!file.exists()) {
                // Nếu thư mục chứa file không tồn tại, tạo thư mục mới
                File parentDir = file.getParentFile();
                if (!parentDir.exists()) {
                    boolean dirCreated = parentDir.mkdirs();
                    if (!dirCreated) {
                        JOptionPane.showMessageDialog(this, "Không thể tạo thư mục mới.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
                // Nếu file không tồn tại, tạo file mới
                boolean created = file.createNewFile();
                if (!created) {
                    JOptionPane.showMessageDialog(this, "Không thể tạo file mới.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            FileOutputStream fos = new FileOutputStream(tenFile);
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            // Ghi danh sách sách vào file
            for (Sach s : ds.getDssach()) {
                oos.writeObject(s);
            }
            oos.close();
            JOptionPane.showMessageDialog(this, "Đã lưu danh sách sách vào file.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi lưu dữ liệu vào file.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void taiDanhSachTuFile() {
        try {
            File file = new File(tenFile);
            if (!file.exists()) {
                return;
            }

            FileInputStream fis = new FileInputStream(file);
            ObjectInputStream ois = new ObjectInputStream(fis);
            // Đọc các đối tượng từ file và thêm vào danh sách
            while (true) {
                try {
                    Sach s = (Sach) ois.readObject();
                    ds.Themsach(s);
                } catch (EOFException e) {
                    break;
                }
            }
            ois.close();

            CapNhatBang();

        } catch (IOException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu từ file.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void CapNhatBang() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        List<Sach> sachList = ds.getDssach(); // Lấy danh sách sách từ DSSach
        for (Sach s : sachList) {
            Object[] row = {s.getMaSach(), s.getTenSach(), s.getTacGia(), s.getNXB(), s.getGia()};
            model.addRow(row);
        }
    }
    

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // thêm sách
        int ma, gia;
        boolean flag = false;
        String ma_txt =text_ma.getText();
        String ten = text_ten.getText();
        String tacgia = text_tacgia.getText();
        String nxb = text_nxb.getText();
        String gia_txt = text_gia.getText();
        
        if (ma_txt.isEmpty() || ten.isEmpty() || tacgia.isEmpty() || nxb.isEmpty() || gia_txt.isEmpty()){
            JOptionPane.showMessageDialog(this, "Không nhập đủ thông tin");
        }
        else {
            try {
                gia = Integer.parseInt(gia_txt);
                ma = Integer.parseInt(ma_txt);
            } catch (NumberFormatException e) {
                ma = 0;
                gia = 0;
                flag = true;
                JOptionPane.showMessageDialog(null, "Mã hoặc giá nhập không phải là số!");
            }
            if (flag == false) {
                Sach sach = new Sach(ma, ten, tacgia, nxb, gia);
                ds.Themsach(sach);

                DefaultTableModel model = (DefaultTableModel) table.getModel();
                clearTextField();
                CapNhatBang();
            }
        }   
    }//GEN-LAST:event_btn_themActionPerformed


    private void hienThiTenBN(String mabn) throws SQLException {
        String tenbn = benhNhanBUS.layTenBN(mabn);
        if (tenbn != null) {
            txt_tenbn.setText(tenbn);
            txt_tenbn.setEditable(false);
        }
        else {
            JOptionPane.showMessageDialog(null, "Bệnh nhân chưa đăng ký", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            txt_tenbn.setText("");
        }
    }
    
    private void loadTenBS() throws SQLException {
        List<String> danhSachTenBS = bacSiBUS.layDSTenBS();
        combo_tenbs.removeAllItems();
        for (String tenBS : danhSachTenBS) {
            combo_tenbs.addItem(tenBS);
        }
    }


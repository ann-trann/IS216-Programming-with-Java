    private void btn_datlichActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_datlichActionPerformed
        
        String mabn = txt_mabn.getText();
        String tenbs = (String) combo_tenbs.getSelectedItem();
        String makb = null;
        String mabs = null;
        boolean flag = true;
        
        try {
            makb = khamBenhBUS.layMaKB();
        } catch (SQLException ex) {
            Logger.getLogger(DatLichKhamGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            mabs = bacSiBUS.layMaBS(tenbs);
        } catch (SQLException ex) {
            Logger.getLogger(DatLichKhamGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Date seletedDate = date_nkham.getDate();
        LocalDate nkham = null;
        if (seletedDate == null || seletedDate.before(Date.from(LocalDate.now().atStartOfDay(ZoneId.systemDefault()).toInstant()))) {
            flag = false;
        }
        else {
            nkham = seletedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        }
        
        String yckham = txt_yckham.getText();
        if (yckham.equals("")) {
            flag = false;
        }
        
        try {
            if (flag) {
                KhamBenhDTO kb = new KhamBenhDTO(makb, mabn, mabs, nkham, yckham, null, 0);
                KhamBenhBUS bus = new KhamBenhBUS();

                if (bus.addKhamBenh(kb)) {
                    JOptionPane.showMessageDialog(this, "Đặt lịch khám thành công!");
                    clearLK();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Đặt lịch khám không thành công!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btn_datlichActionPerformed

    private void clearLK(){
        txt_mabn.setText("");
        txt_tenbn.setText("");
        txt_yckham.setText("");
        date_nkham.setDate(null);
        combo_tenbs.setSelectedIndex(0);
   }
